#!/usr/bin/env python3
"""
A function that accepts a float and returns the floor
"""
import math


def floor(n: float) -> int:
    """
    Parameters:
    n (float) --> float value
    
    Method:
    Use math.floor to retrieve the
    floor of the float (n)

    Returns:
    The floor of the float
    """
    return (math.floor(n))
