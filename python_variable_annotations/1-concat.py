#!/usr/bin/env python3
"""
A function that takes a string and concatenates it
"""


def concat(str1: str, str2: str) -> str:
    """
    Parameters:
    str1 (string) --> the first string
    str2 (string) --> the second string

    Returns:
    The concatenation of the two strings
    """
    return (str1 + str2)
