#!/usr/bin/env python3
"""
A function that returns the string representation of a float
"""


def to_str(n: float) -> str:
    """
    Parameters:
    n (float) --> float value
    
    Method:
    Use the built in str() function to retrieve
    the string representation of the float

    Returns:
    String representation of the float
    """
    return (str(n))
